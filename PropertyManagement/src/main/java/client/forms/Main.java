    private void jbSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveActionPerformed
        // TODO add your handling code here:
        Propertie toSave = propertieBuilderForm
                .setTitle(jtxTitle.getText())
                .setAddress(jtxAddress.getText())
                .setNumberBedroom(Integer.parseInt(jsNumberBedrooms.getValue().toString()))
                .setNumberGarage(Integer.parseInt(jsNumberGarages.getValue().toString()))
                .setLandArea(Integer.parseInt(jtxLandArea.getText()))
                .setValue(Double.parseDouble(jtxValue.getText()))
                .setBuildingArea(Integer.parseInt(jtxBuildedArea.getText()))
                .setType(jcbType.getSelectedItem().equals("Apartamento")?Propertie.Type.APARTMENT:Propertie.Type.HOUSE)
                .build();
        System.out.println(toSave.getType());
        if (toSave.getId() == 0) {
            client.createPropertie(toSave);
        } else {
            client.updatePropertie(toSave);
        }
        JOptionPane.showMessageDialog(null, "Salvo com sucesso!", "Sucesso", JOptionPane.PLAIN_MESSAGE);
        clearForm();
        fillTable();
        

    }//GEN-LAST:event_jbSaveActionPerformed

