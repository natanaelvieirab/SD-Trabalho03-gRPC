    private void clearForm() {
        jtxValue.setText("");
        jtxAddress.setText("");
        jtxBuildedArea.setText("");
        jtxLandArea.setText("");
        jtxTitle.setText("");
        jsNumberBedrooms.setValue(0);
        jsNumberGarages.setValue(0);
        propertieBuilderForm = Propertie.newBuilder();
    }

    public void fillTable() {

        myList = this.toPropertieModelList(client.listAllPropertie());
        MyTableModel mtb = new MyTableModel(PropertieModel.class, myList, jtProperties);
        jtProperties.setModel(mtb);
    }

    private List<PropertieModel> toPropertieModelList(List<Propertie> list) {
        List<PropertieModel> toReturn = new ArrayList<>();
        for (Propertie prop : list) {
            toReturn.add(toPropertieModel(prop));
        }
        return toReturn;
    }

    private PropertieModel toPropertieModel(Propertie prop) {

        return new PropertieModel(prop.getId(), prop.getTitle(), prop.getAddress(), prop.getType().equals(Propertie.Type.APARTMENT)?"Apartamento":"Casa", prop.getNumberBedroom(), prop.getNumberGarage(), prop.getLandArea(), prop.getBuildingArea(), prop.getValue());

    }

    private Propertie toPropertie(PropertieModel prop) {

        return Propertie.newBuilder()
                .setId(prop.getId())
                .setTitle(prop.getTitle())
                .setAddress(prop.getAddress())
                .setNumberBedroom(prop.getNumberBedroom())
                .setNumberGarage(prop.getNumberGarage())
                .setLandArea(prop.getLandArea())
                .setValue(prop.getValue())
                .setType(jcbType.getSelectedItem().equals("Apartamento")?Propertie.Type.APARTMENT:Propertie.Type.HOUSE)
                .build();

    }

